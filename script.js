const add = (num1, num2) => {
  return num1 + num2;
};

const subtract = (num1, num2) => {
  return num1 - num2;
};

const multiply = (num1, num2) => {
  return num1 * num2;
};

const divide = (num1, num2) => {
  return num1 / num2;
};

console.log(add(1, 3));
console.log(subtract(3, 1));
console.log(multiply(2, 3));
console.log(divide(9,3));
